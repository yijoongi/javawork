package basic;

public class AssignmentOperater {

	public static void main(String[] args) {
		// 대입(할당)연산자 : =
		// 복합대입연산자 (+= , =+, *= ,/=, %=) ( /(슬래시)로 나누면 몫 ,
		//(%(퍼센트)로 나누면 나머지를 불러옴.
		//- 변수에 값을 대입할때 사용하는 연산자
		//- 복합대입연산자 : 대입 연산자에 산술연산자가 결합된다
		

		int  a = 5;
		int  b = 5;
		
		a += 3; // a = a + 3(누적연산)
		System.out.println(a);
		System.out.println("----------------------------------");
		
		a -= 4; // a = a - 4
		System.out.println(a);
		
		a *= 6; // a = a * 6
		System.out.println(a);
		
		a /= 5; // a = a / 5
		System.out.println(a);
		
		a %= 3; //a = a % 3
		System.out.println(a);
		
		
		
		
		
		
		
		
		
		
	}

}
